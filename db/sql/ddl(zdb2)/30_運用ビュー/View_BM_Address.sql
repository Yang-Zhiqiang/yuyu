     knadrketasuu ,         /* カナ住所桁数 */
     todouhukencd ,         /* 都道府県コード */
     gyouseikukakucd ,         /* 行政区画コード */
     ikoumaekihontikucd ,         /* 移行前基本地区コード */
     newynohyouji ,         /* 新郵便番号表示 */
     adrkj ,         /* 住所（漢字） */
     adrkn ,         /* 住所（カナ） */
     adrknblankari ,         /* 住所（カナ）（空白あり） */
     kousinsyaId ,         /* 更新者ＩＤ */
     kousinTime ,         /* 更新時間 */
     kousinKinou ,         /* 更新機能 */
