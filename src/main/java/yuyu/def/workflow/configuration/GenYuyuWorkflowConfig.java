package yuyu.def.workflow.configuration;


/**
 * workflowパッケージ用 システム情報格納部品です。<br />
 * このソースはツールにより自動生成されたものです。業務静的定義に変更があると上書きされてしまいます。
 */
public class GenYuyuWorkflowConfig {

    public GenYuyuWorkflowConfig() {
    }

    private boolean workflowStubMode;

    public boolean isWorkflowStubMode() {
        return workflowStubMode;
    }

    public void setWorkflowStubMode(boolean pWorkflowStubMode) {
        workflowStubMode = pWorkflowStubMode;
    }

    private String dummyFilePath;

    public String getDummyFilePath() {
        return dummyFilePath;
    }

    public void setDummyFilePath(String pDummyFilePath) {
        dummyFilePath = pDummyFilePath;
    }
}
