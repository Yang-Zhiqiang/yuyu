package yuyu.def.qrcode.configuration;


/**
 * qrcodeパッケージ用 システム情報格納部品です。<br />
 * このソースはツールにより自動生成されたものです。業務静的定義に変更があると上書きされてしまいます。
 */
public class GenYuyuQrcodeConfig {

    public GenYuyuQrcodeConfig() {
    }

    private String errorCorrectionLevel;

    public String getErrorCorrectionLevel() {
        return errorCorrectionLevel;
    }

    public void setErrorCorrectionLevel(String pErrorCorrectionLevel) {
        errorCorrectionLevel = pErrorCorrectionLevel;
    }

    private String width;

    public String getWidth() {
        return width;
    }

    public void setWidth(String pWidth) {
        width = pWidth;
    }

    private String height;

    public String getHeight() {
        return height;
    }

    public void setHeight(String pHeight) {
        height = pHeight;
    }

    private String encodeType;

    public String getEncodeType() {
        return encodeType;
    }

    public void setEncodeType(String pEncodeType) {
        encodeType = pEncodeType;
    }

    private String fileType;

    public String getFileType() {
        return fileType;
    }

    public void setFileType(String pFileType) {
        fileType = pFileType;
    }

    private String filePath;

    public String getFilePath() {
        return filePath;
    }

    public void setFilePath(String pFilePath) {
        filePath = pFilePath;
    }
}
